# Solicitação de inserção do primeiro nome
primeiro_nome = input("Digite seu primeiro nome: ")

# Solicitação de inserção do sobrenome
sobrenome = input("Digite seu sobrenome: ")

# Variável que conncatena o primeiro nome com o sobrenome
nome_completo = primeiro_nome + " " + sobrenome

# Exibição do resultado utilizando f-strings
print(f"Seu nome completo é: {nome_completo}")