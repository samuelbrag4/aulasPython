# Solicitção da inserção de um número (o float é para aceitar números decimais)
numero = float(input("Digite um número: "))

# Solicitção da inserção de outro número (o float é para aceitar números decimais)
outro_numero = float(input("Digite outro número: "))

# Somando os dois números
soma = numero + outro_numero

# Exibição
print(f"A soma dos dois números é: {soma}")