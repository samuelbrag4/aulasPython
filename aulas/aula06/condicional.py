# Operadores condicionais em Python

'''
Os comparadores de variáveis em python são:
    
        == : Igual a
        != : Diferente de
        > : Maior que
        < : Menor que
        >= : Maior ou igual a
        <= : Menor ou igual a
        
Eles ajudam a fazer operações 'booleanas' que retornam True ou False.
Comparando elementos e retornando True ou False.
'''

# Testes:

# Igual a:
print(2 == 2)

# Diferente de:
print(2 != 2)

# Maior que:
print(2 > 2)

# Menor que:
print(2 < 2)

# Maior ou igual a:
print(2 >= 2)

# Menor ou igual a:
print(2 <= 2)