# Módulos e Funções de Biblioteca

'''
Uma biblioteca é um conjunto de módulos que podem ser importados para o seu programa.

Algumas bibliotecas populares e o que fazem:
1- math: funções matemáticas
2- random: funções para gerar números aleatórios
3- time: funções para manipular datas e horas
4- os: funções para manipular arquivos e diretórios
5- sys: funções para manipular o interpretador Python

Para importar uma biblioteca, use o comando import seguido do nome da biblioteca.

Exemplo:
import math # importa a biblioteca math
'''

from math import sqrt

# -----sqrt----- é uma função da biblioteca math que calcula a raiz quadrada de um número.

resultado = sqrt(25)
print(resultado)

