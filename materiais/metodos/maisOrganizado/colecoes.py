# Método all()
# Retorna True se todos os elementos de um iterável forem verdadeiros (ou se o iterável estiver vazio).
# Exemplo:
lista = [True, True, True]
print(all(lista))  # Saída: True

# Método any()
# Retorna True se algum elemento de um iterável for verdadeiro. Se o iterável estiver vazio, retorna False.
# Exemplo:
lista = [False, True, False]
print(any(lista))  # Saída: True

# Método len()
# Retorna o número de itens em um objeto.
# Exemplo:
lista = [1, 2, 3]
print(len(lista))  # Saída: 3

# Método max()
# Retorna o maior item em um iterável ou o maior de dois ou mais argumentos.
# Exemplo:
lista = [1, 2, 3]
print(max(lista))  # Saída: 3

# Método min()
# Retorna o menor item em um iterável ou o menor de dois ou mais argumentos.
# Exemplo:
lista = [1, 2, 3]
print(min(lista))  # Saída: 1